#include <stdio.h>
#include <limits.h>

int	    is_power_of_2(unsigned int n)
{
	if (n == 0)
		return (0);
	return (n & (n - 1) == 0);
}

int main()
{
	printf("%d\n", is_power_of_2(4294967295));
	// printf("%d\n", INT_MAX);
}
