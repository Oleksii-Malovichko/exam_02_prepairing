#include <unistd.h>

int main(int n, char **args)
{
	if (n == 2)
	{
		int i = 0;
		while (args[1][i] != '\0')
		{
			if (args[1][i] >= 'A' && args[1][i] <= 'Z')
			{
				write(1, "_", 1);
				args[1][i] += 32;
			}
			write(1, &args[1][i], 1);
			i++;
		}
	}
	write(1, "\n", 1);
}