#include <stdio.h>

int	ft_atoi(const char *str)
{
	int	result = 0;
	int	i = 0;
	int sign = 1;

	while (*str == ' ' || (*str >= 9 && *str <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
		{
			sign = -sign;
		}
		i++;
	}
	while (str[i] != '\0' && str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	return (sign * result);
}

int main()
{
	printf("%d\n", ft_atoi("-255"));
}