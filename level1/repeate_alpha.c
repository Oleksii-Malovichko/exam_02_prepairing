#include <unistd.h>

int	main(int n, char **args)
{
	int	q = 0;
	int i = 0;

	if (n == 2)
	{
		while (args[1][i] != '\0')
		{
			if (args[1][i] >= 'A' && args[1][i] <= 'Z')
			{
				q = args[1][i] - 'A';
			}
			else if (args[1][i] >= 'a' && args[1][i] <= 'z')
			{
				q = args[1][i] - 'a';
			}
			else
				write(1, &args[1][i], 1);
			while (q >= 0)
			{
				write(1, &args[1][i], 1);
				q--;
			}
			i++;
		}
	}
	write(1, "\n", 1);
}
